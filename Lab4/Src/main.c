// Function to set PWM duty cycle
void Set_LED_Brightness(uint32_t adc_value, TIM_HandleTypeDef *htim, uint32_t channel) {
    // Scale ADC (0-4095) to PWM (0-1000) for a 0-100% duty cycle
    uint32_t duty_cycle = (adc_value * 1000) / 4095;
    __HAL_TIM_SET_COMPARE(htim, channel, duty_cycle);
}

// ADC Conversion Complete Callback
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc) {
    if (hadc->Instance == ADC1) {
    	uint32_t pot1_value = HAL_ADC_GetValue(hadc);  // Read current ADC value
    	        static uint8_t current_channel = 0;

    	        switch (current_channel) {
    	            case 0:
    	                Set_LED_Brightness(pot1_value, &htim4, TIM_CHANNEL_1); // LED1
    	                current_channel = 1;
    	                break;
    	            case 1:
    	                Set_LED_Brightness(pot1_value, &htim4, TIM_CHANNEL_2); // LED2
    	                current_channel = 2;
    	                break;
    	            case 2:
    	                Set_LED_Brightness(pot1_value, &htim4, TIM_CHANNEL_3); // LED3
    	                current_channel = 0;
    	                break;
    	        }
    	        HAL_ADC_Start_IT(hadc); // Start next ADC conversion
    }
}

